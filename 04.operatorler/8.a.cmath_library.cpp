#include <iostream>
#include <cmath>

using namespace std;

int main()
{

    // abs(x) mutlak değer alma fonksiyonu
    cout << "abs(3-5): " << abs(3-5) << endl;

    // ceil(x) en yakın yukarı tam sayıya yuvarlarma
    cout << "ceil(3.4: " << ceil(3.4) << endl;
    cout << "ceil(5.6): " << ceil(5.6) << endl;

    // floor(x) en yakın alt tam sayıya yuvarlarma
    cout << "floor(3.4: " << floor(3.4) << endl;
    cout << "floor(5.6): " << floor(5.6) << endl;

    // pow(x,y) x sayısının y'ci kuvvetini döndürür
    cout << "pow(2,5): " << pow(2,5) << endl;
    cout << "pow(8,2): " << pow(8,2) << endl;

    return 0;

}

