/*
    1 ile 100 arası çift sayıları ekrana yazdıran program
    +2 artimla

                                                        Kamil Bala
*/
#include<iostream>
using namespace std;

int sayac = 1;

int main()
{
    for(int sayac=0; sayac<=100;sayac+=2)
    {
        cout<<sayac<<endl;
    }

    return 0;
}
